my0=1;
mx1=3;
my1=-1;
sx=sqrt(3/2);
sy=sqrt(2);
rxy=1/sqrt(3);
a=(2*(mx0-mx1))/(sx)^2+(2*rxy*(my1-my0))/sx*sy;
b=(2*(my0-my1))/(sy)^2+(2*rxy*(mx1-mx0))/sx*sy;
c=((mx1)^2-(mx0)^2)/(sx)^2+(2*rxy*(mx0*my0-mx1*my1))/sx*sy+((my1)^2-(my0)^2)/(sy)^2;
x=-10:0.1:10;
y=-(a*x+c)/b;
plot(partb_H0_data(:,1),partb_H0_data(:,2),'o',partb_H1_data(:,1),partb_H1_data(:,2),'+',x,y)
xlabel('x')
ylabel('y')
legend('H0','H1')
