mx0=mean(partc_H0_data(:,1));
my0=mean(partc_H0_data(:,2));
mx1=mean(partc_H1_data(:,1));
my1=mean(partc_H1_data(:,2));
sx=sqrt(2);
sy=1;
rxy=0;
a=(2*(mx0-mx1))/(sx)^2+(2*rxy*(my1-my0))/sx*sy;
b=(2*(my0-my1))/(sy)^2+(2*rxy*(mx1-mx0))/sx*sy;
c=((mx1)^2-(mx0)^2)/(sx)^2+(2*rxy*(mx0*my0-mx1*my1))/sx*sy+((my1)^2-(my0)^2)/(sy)^2;
x=-10:0.1:10;
y=-(a*x+c)/b;
plot(partc_H0_data(:,1),partc_H0_data(:,2),'o',partc_H1_data(:,1),partc_H1_data(:,2),'+',x,y)
xlabel('x')
ylabel('y')
legend('H0','H1')
H0_decision=(a*partc_H0_data(:,1)+b*partc_H0_data(:,2)+c)>0;
missH0=length(H0_decision)-sum(H0_decision);
H1_decision=(a*partc_H1_data(:,1)+b*partc_H1_data(:,2)+c)>0;
missH1=sum(H1_decision);
